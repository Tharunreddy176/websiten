<h2 class="wp-block-heading"><strong>Take Comfort in Knowing You Have Financial Support for Accidents</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Did you know that accident insurance coverage can help supplement costs that are not covered under your existing medical plan? This insurance provides you with funds that can be used as you see fit.<sup>1</sup>&nbsp;This can help with out-of-pocket expenses like deductibles, copays, medical transportation, childcare, and more.</p>
